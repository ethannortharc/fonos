    fn text_actions_map_to_workflows_with_target_outputs_and_clear() {
        let mut cfg = AppConfig::default();
        cfg.text_actions = vec![
            TextActionBinding { hotkey: "cmd+1".into(), mode_id: "polish".into(), output_target: OutputTarget::FloatingPopup },
            TextActionBinding { hotkey: "cmd+2".into(), mode_id: "formal".into(), output_target: OutputTarget::ActiveTextField },
            TextActionBinding { hotkey: "cmd+3".into(), mode_id: "translate".into(), output_target: OutputTarget::Clipboard },
            TextActionBinding { hotkey: "cmd+4".into(), mode_id: "summarize".into(), output_target: OutputTarget::AppendToContainer },
            TextActionBinding { hotkey: "cmd+5".into(), mode_id: "polish".into(), output_target: OutputTarget::None },
        ];

        assert!(migrate_to_workflows(&mut cfg, &BTreeMap::new()));

        let ta = |i: usize| -> &WorkflowDef {
            cfg.workflows
                .iter()
                .find(|w| w.id == format!("wf.ta-{i}"))
                .unwrap_or_else(|| panic!("wf.ta-{i} missing"))
        };

        // index 0: floating_popup → out.panel.
        let t0 = ta(0);
        assert_eq!(t0.hotkey, "cmd+1");
        assert_eq!(t0.source, "src.selection");
        assert_eq!(t0.processors, vec!["llm.polish".to_string()]);
        assert_eq!(t0.outputs, vec!["out.panel".to_string()]);
        assert_eq!(t0.name, "文本动作 Polish");
        assert!(!t0.builtin);

        // The full five-target mapping.
        assert_eq!(ta(1).outputs, vec!["out.insert".to_string()], "active_text_field → out.insert");
        assert_eq!(ta(2).outputs, vec!["out.clipboard".to_string()], "clipboard → out.clipboard");
        assert_eq!(ta(3).outputs, vec!["out.quicknote".to_string()], "append_to_container → out.quicknote");
        assert_eq!(ta(4).outputs, vec!["out.clipboard".to_string()], "none → out.clipboard");

        // Processor ids and hotkeys carried through.
        assert_eq!(ta(1).processors, vec!["llm.formal".to_string()]);
        assert_eq!(ta(2).processors, vec!["llm.translate".to_string()]);
        assert_eq!(ta(3).processors, vec!["llm.summarize".to_string()]);
        assert_eq!(ta(4).hotkey, "cmd+5");

        // Legacy text_actions are cleared to prevent double-registration.
        assert!(cfg.text_actions.is_empty());
    }

