    // ── Rule: default config produces the expected full shape ───────────────

    #[test]
    fn migrate_default_config_produces_expected_shape() {
        let mut cfg = AppConfig::default();
        let custom = BTreeMap::new();

        let changed = migrate_to_workflows(&mut cfg, &custom);
        assert!(changed, "first migration reports a change");
        assert!(cfg.workflow_migration_done, "migration flag is set");

        // Exactly three workflow overlays for the default config: dictation,
        // note, listen (no toggle, no notebook-N, no text-actions).
        assert_eq!(cfg.workflows.len(), 3, "dictation + note + listen only");
        // Exactly one widget overlay: the out.speak listen-voice override.
        assert_eq!(cfg.widgets.len(), 1, "only the out.speak override");

        // wf.dictation overlay: the dictation hotkey lands here; raw mode ⇒ no
        // LLM step; mic-hold source; insert output; builtin preserved.
        let d = cfg.workflows.iter().find(|w| w.id == "wf.dictation").expect("wf.dictation");
        assert_eq!(d.hotkey, "cmd+shift+space");
        assert_eq!(d.source, "src.mic-hold");
        assert_eq!(d.processors, vec!["stt.default".to_string()]);
        assert_eq!(d.outputs, vec!["out.insert".to_string()]);
        assert!(d.builtin);

        // No toggle workflow: hotkey_dictation_toggle is empty by default.
        assert!(!cfg.workflows.iter().any(|w| w.id == "wf.dictation-toggle"));

        // wf.note overlay: note hotkey; STT only (no LLM — faithful); quicknote.
        let n = cfg.workflows.iter().find(|w| w.id == "wf.note").expect("wf.note");
        assert_eq!(n.hotkey, "option+n");
        assert_eq!(n.source, "src.mic-hold");
        assert_eq!(n.processors, vec!["stt.default".to_string()]);
        assert_eq!(n.outputs, vec!["out.quicknote".to_string()]);
        assert!(n.builtin);

        // No notebook-N workflows: notebook_hotkey_N are all 0 by default.
        assert!(!cfg.workflows.iter().any(|w| w.id.starts_with("wf.note-")));

        // wf.listen overlay: listen hotkey; llm.listen; speak output.
        let l = cfg.workflows.iter().find(|w| w.id == "wf.listen").expect("wf.listen");
        assert_eq!(l.hotkey, "option+l");
        assert_eq!(l.source, "src.selection");
        assert_eq!(l.processors, vec!["llm.listen".to_string()]);
        assert_eq!(l.outputs, vec!["out.speak".to_string()]);
        assert!(l.builtin);

        // out.speak override carries the (default) listen voice settings.
        let sp = cfg.widgets.iter().find(|w| w.id == "out.speak").expect("out.speak override");
        assert_eq!(sp.role, WidgetRole::Output);
        assert_eq!(sp.type_tag, "speak");
        assert_eq!(sp.props["voice_profile"], "");
        assert_eq!(sp.props["voice"], "default");

        // No text actions by default.
        assert!(!cfg.workflows.iter().any(|w| w.id.starts_with("wf.ta-")));
        assert!(cfg.text_actions.is_empty());

        // Rule 6: legacy hotkey fields are cleared; dictation_mode is preserved.
        assert!(cfg.hotkey_dictation.is_empty());
        assert!(cfg.hotkey_dictation_toggle.is_empty());
        assert!(cfg.hotkey_note.is_empty());
        assert!(cfg.hotkey_note_1.is_empty());
        assert!(cfg.hotkey_note_2.is_empty());
        assert!(cfg.hotkey_note_3.is_empty());
        assert!(cfg.hotkey_listen.is_empty());
        assert_eq!(cfg.dictation_mode, "raw");
    }

    // ── Rule: idempotency ───────────────────────────────────────────────────

    #[test]
    fn migrate_is_idempotent() {
        let mut cfg = AppConfig::default();
        let custom = BTreeMap::new();

        assert!(migrate_to_workflows(&mut cfg, &custom), "first call changes config");
        let workflows_after_first = cfg.workflows.clone();
        let widgets_after_first = cfg.widgets.clone();
        let dictation_mode_after_first = cfg.dictation_mode.clone();

        // Second call is a no-op: returns false, mutates nothing.
        assert!(!migrate_to_workflows(&mut cfg, &custom), "second call reports no change");
        assert_eq!(cfg.workflows, workflows_after_first, "workflows unchanged");
        assert_eq!(cfg.widgets, widgets_after_first, "widgets unchanged");
        assert_eq!(cfg.dictation_mode, dictation_mode_after_first);
        assert!(cfg.workflow_migration_done);
    }

    #[test]
    fn migrate_returns_false_when_already_done() {
        let mut cfg = AppConfig { workflow_migration_done: true, ..Default::default() };
        // Pre-set flag ⇒ short-circuit; the dictation hotkey is left untouched.
        assert!(!migrate_to_workflows(&mut cfg, &BTreeMap::new()));
        assert_eq!(cfg.hotkey_dictation, "cmd+shift+space");
        assert!(cfg.workflows.is_empty());
    }

